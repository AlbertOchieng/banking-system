              if(newBalace<0){
               JOptionPane.showMessageDialog(this, "Account Over Draft", "Error", JOptionPane.ERROR_MESSAGE);
                return;

            }

                String sql2="INSERT  INTO `withdrawals`(id,`AccountNumber`, `Name`, `IdNumber`,`AccountType`, `Date` ,`InitialAmount`,Amount, `NewBalance`) VALUES ('0','" + acnumber.getText() + "','" + jTextFiename.getText() + "','" + jTextidnumber.getText() + "','" + jCombacytpe.getSelectedItem() + "','" + ((JTextField) jDate.getDateEditor().getUiComponent()).getText() + "','"+jTexBalance.getText()+"','" + withdrawamt.getText() + "','"+newBalace+"') ";

                String sql3="UPDATE mainaccount SET  AccountBalance='"+newBalace+"' WHERE AccountNumber='" + acnumber.getText() + "'";
