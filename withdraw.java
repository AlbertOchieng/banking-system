    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {

            con = (com.mysql.jdbc.Connection) DBManager.ConnectDB();


            String sql = "select * from mainaccount where AccountNumber = '" + acnumber.getText() + "'";
            pst = (com.mysql.jdbc.PreparedStatement) con.prepareStatement(sql);
            rs = pst.executeQuery();
            if (rs.next()) {

                String add1 = rs.getString("Name");
                jTextFiename.setText(add1);
                String add11 = rs.getString("IdNumber");
                jTextidnumber.setText(add11);
                String add2 = rs.getString("AccountBalance");
                jTexBalance.setText(add2);
                String add12 = rs.getString("AccountType");
                jCombacytpe.setSelectedItem(add12);
                String add6 = rs.getString("Contact");
                phoneNumber.setText(add6);



            } else {
                JOptionPane.showMessageDialog(this, "Client  with Bank Account Number " + acnumber.getText() + " does not exist","Error message",JOptionPane.WARNING_MESSAGE);
                return;
            }
con.close();
        } catch (SQLException | HeadlessException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

